package com.harivansh.gitinfo.model;

public class Issue {

    private String issueName;

    public Issue(String issueName) {
        this.issueName = issueName;
    }

    public String getIssueName() {
        return issueName;
    }

    public void setIssueName(String issueName) {
        this.issueName = issueName;
    }
}
